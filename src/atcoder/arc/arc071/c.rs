fn main() {
    let n = read();
    let s: Vec<String> = readn(n);

    let mut cnt = vec![255; 26];
    for s in s {
        let mut part = vec![0; 26];
        for x in s.into_bytes() {
            part[(x - 'a' as u8) as usize] += 1;
        }
        for i in 0..26 {
            cnt[i] = min(cnt[i], part[i]);
        }
    }

    for i in 0..26 {
        for _ in 0..cnt[i] {
            print!("{}", ('a' as u8 + i as u8) as char);
        }
    }
    println!();
}